import UIKit

extension UIColor {
    class func snowColor() -> UIColor {
        return UIColor(red: 0.961, green: 0.961, blue: 0.961, alpha: 1.0)
    }
    
    class func slateColor() -> UIColor {
        return UIColor(red: 0.29, green: 0.306, blue: 0.302, alpha: 1.0)
    }
    
    class func goldColor() -> UIColor {
        return UIColor(red:1.00, green:0.84, blue:0.34, alpha:1.0)
    }
    
    class func darkGoldColor() -> UIColor {
        return UIColor(red:0.73, green:0.56, blue:0.00, alpha:1.0)
    }
    
    class func nightColor() -> UIColor {
        return UIColor(white:0.21, alpha:1.0)
    }
    
    class func porcelainColor() -> UIColor {
        return UIColor(red:0.236, green:0.240, blue:0.241, alpha:1.0)
    }
    
    class func whiteSmokeColor() -> UIColor {
        return UIColor(red:0.236, green:0.236, blue:0.236, alpha:1.0)
    }

}